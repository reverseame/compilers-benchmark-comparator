import os
from pathlib import Path
from collections import defaultdict

def generate_latex_figures(root_dir, output_file):
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write("% Auto-generated LaTeX file with all figures\n")
        f.write("% This file was automatically generated by a Python script\n\n")

        root_path = Path(root_dir)
        metric_files = ['File_size_KB.pdf', 'Fortified_Functions.pdf',
                        'Memory_usage_KB.pdf', 'Time_ms.pdf']
        metric_names = {
            'File_size_KB.pdf': 'File Size Kb',
            'Fortified_Functions.pdf': 'Fortified Functions',
            'Memory_usage_KB.pdf': 'Memory Usage Kb',
            'Time_ms.pdf': 'Time Ms'
        }

        metric_files_pct = ['File_size_KB_porcentaje.pdf', 'Fortified_Functions_porcentaje.pdf',
                            'Memory_usage_KB_porcentaje.pdf', 'Time_ms_porcentaje.pdf']
        metric_names_pct = {
            'File_size_KB_porcentaje.pdf': 'File Size Kb Porcentaje',
            'Fortified_Functions_porcentaje.pdf': 'Fortified Functions Porcentaje',
            'Memory_usage_KB_porcentaje.pdf': 'Memory Usage Kb Porcentaje',
            'Time_ms_porcentaje.pdf': 'Time Ms Porcentaje'
        }

        dir_groups = dict()
        individual_files = defaultdict(list)

        # First collect all directories and files
        for dirpath, _, filenames in os.walk(root_dir):
            pdf_files = [fn for fn in filenames if fn.lower().endswith('.pdf') and fn != 'valores_por_defecto.pdf']
            if not pdf_files:
                continue

            rel_path = Path(dirpath).relative_to(root_dir)

            if all(m in pdf_files for m in metric_files):
                dir_groups[rel_path] = (metric_files, metric_names)
            elif all(m in pdf_files for m in metric_files_pct):
                dir_groups[rel_path] = (metric_files_pct, metric_names_pct)
            else:
                for pdf_file in sorted(pdf_files):
                    individual_files[rel_path / pdf_file[:-4]].append(pdf_file)

        # First: render individual figures
        for group_path, pdf_list in individual_files.items():
            for pdf_file in pdf_list:
                label = f"fig:{root_path.name}_{str(group_path).replace(os.path.sep, '_').replace('-', '_').lower()}"
                caption = group_path.name.replace("_", " ").title()
                full_pdf_path = f"{root_path.name}/{group_path.parent}/{group_path.name}.pdf"

                parent_parts = []
                for part in group_path.parent.parts:
                    if not part.startswith('Gráficas'):
                        parent_parts.append(part.replace('_', ' '))

                if parent_parts:
                    title = " / ".join(parent_parts) + " / " + caption
                else:
                    title = caption

                f.write(f"\n% === {group_path} ===\n")
                f.write(f"\\subsection*{{{title}}}\n")
                f.write("\\begin{figure}[htbp]\n")
                f.write("  \\centering\n")
                f.write(f"  \\includegraphics[width=\\textwidth, height=0.85\\textheight, keepaspectratio]{{{full_pdf_path}}}\n")
                f.write(f"  \\caption{{{caption}}}\n")
                f.write(f"  \\label{{{label}}}\n")
                f.write("\\end{figure}\n\\newpage\n")

        # Define the order of sections
        section_order = [
            "Gráficas_por_optimización",
            "Gráficas_por_medida_seguridad",
            "Gráficas_por_optimización_porcentual",
            "Gráficas_por_medida_seguridad_porcentual"
        ]

        # Then: render grouped figures (sets of 4) in the specified order
        shown_sections = set()
        
        # Process sections in the specified order
        for section in section_order:
            # Find all paths that belong to this section
            section_paths = [p for p in dir_groups.keys() if p.parts and p.parts[0] == section]
            
            if not section_paths:
                continue
                
            # Write section header if not already shown
            if section not in shown_sections:
                f.write(f"\n\\section*{{{section.replace('_', ' ').title()}}}\n")
                shown_sections.add(section)
                
            # Sort paths for consistent ordering
            section_paths_sorted = sorted(section_paths)
            
            for group_path in section_paths_sorted:
                pdf_files, names_dict = dir_groups[group_path]
                
                parent_parts = []
                for part in group_path.parts[:-1]:
                    if not part.startswith('Gráficas'):
                        parent_parts.append(part.replace('_', ' '))

                group_name = str(group_path.name).replace('_', ' ')
                if group_name.startswith('-'):
                    group_name = group_name.upper()

                if parent_parts:
                    full_title = " / ".join(parent_parts) + " / " + group_name
                else:
                    full_title = group_name

                f.write(f"\n% === {group_path} ===\n")
                f.write(f"\\subsection*{{{full_title}}}\n")
                f.write("\\begin{figure}[htbp]\n")
                f.write("  \\centering\n")

                caption_text = f"Resultados para {parent_parts[-1]} ({group_name})" if parent_parts else f"Resultados de compilación con optimización {group_name}"
                f.write(f"  \\caption{{{caption_text}}}\n")
                f.write("  \\vspace{0.5cm}\n\n")

                # First row
                for i in range(2):
                    f.write("  \\begin{minipage}{0.48\\textwidth}\n")
                    f.write("    \\centering\n")
                    pdf_file = pdf_files[i]
                    label = f"fig:{root_path.name}_{str(group_path).replace(os.path.sep, '_').replace('-', '_').lower()}_{pdf_file[:-4].lower()}"
                    f.write(f"    \\subfloat[{names_dict[pdf_file]}]{{\n")
                    f.write(f"      \\includegraphics[width=\\linewidth]{{{root_path.name}/{group_path}/{pdf_file}}}\n")
                    f.write(f"      \\label{{{label}}}\n")
                    f.write("    }\n")
                    f.write("  \\end{minipage}\n")
                    if i == 0:
                        f.write("  \\hfill\n")
                    else:
                        f.write("\n\n")

                f.write("  \\vspace{1cm}\n\n")

                # Second row
                for i in range(2, 4):
                    f.write("  \\begin{minipage}{0.48\\textwidth}\n")
                    f.write("    \\centering\n")
                    pdf_file = pdf_files[i]
                    label = f"fig:{root_path.name}_{str(group_path).replace(os.path.sep, '_').replace('-', '_').lower()}_{pdf_file[:-4].lower()}"
                    f.write(f"    \\subfloat[{names_dict[pdf_file]}]{{\n")
                    f.write(f"      \\includegraphics[width=\\linewidth]{{{root_path.name}/{group_path}/{pdf_file}}}\n")
                    f.write(f"      \\label{{{label}}}\n")
                    f.write("    }\n")
                    f.write("  \\end{minipage}\n")
                    if i == 2:
                        f.write("  \\hfill\n")
                    else:
                        f.write("\n")

                f.write("\\end{figure}\n\\newpage\n")

if __name__ == "__main__":
    graphics_dir = "Gráficas"
    output_tex_file = "all_figures.tex"

    generate_latex_figures(graphics_dir, output_tex_file)
    print(f"Generated LaTeX file with grouped figures: {output_tex_file}")
